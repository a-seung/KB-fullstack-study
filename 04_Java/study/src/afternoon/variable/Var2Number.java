package afternoon.variable;

public class Var2Number {
    public static void main(String[] args) {
        // 정수
        byte b = 127;
        short s = 32767;
        int i = 2147483637;
        long l = 9223372036854775807L;

        // 실수
        float f = 4.3f; //소수점 7째자리까지, 반드시 뒤에 f로 표기해줘야함
        double d = 4.3; //소수점 15째자리까지

    }
}
